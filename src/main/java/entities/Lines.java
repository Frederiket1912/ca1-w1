package entities;

import java.io.Serializable;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.Table;
import javax.validation.constraints.NotNull;
import javax.validation.constraints.Size;
import javax.xml.bind.annotation.XmlRootElement;

/**
 *
 * @author Malthe
 */
@Entity
@Table(name = "LINES")
@XmlRootElement
@NamedQueries({
    @NamedQuery(name = "Lines.findAll", query = "SELECT l FROM Lines l"),
    @NamedQuery(name = "Lines.findById", query = "SELECT l FROM Lines l WHERE l.id = :id"),
    @NamedQuery(name = "Lines.findByLine", query = "SELECT l FROM Lines l WHERE l.line = :line")})
public class Lines implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @Basic(optional = false)
    @NotNull
    @Column(name = "id")
    private Integer id;
    @Size(max = 255)
    @Column(name = "line")
    private String line;

    public Lines() {
    }

    public Lines(Integer id) {
        this.id = id;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getLine() {
        return line;
    }

    public void setLine(String line) {
        this.line = line;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof Lines)) {
            return false;
        }
        Lines other = (Lines) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "entities.Lines[ id=" + id + " pickupline=" + line + " ]";
    }
    
}
